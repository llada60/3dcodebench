def make_tube(p0, p1, r0, r1=None, verts=8, name="tube"):
    if r1 is None: r1 = r0
    p0, p1 = Vector(p0), Vector(p1)
    direction = p1 - p0
    length = direction.length
    if length < 1e-5: return None
    direction /= length
    center = (p0 + p1) * 0.5

    bm = bmesh.new()
    bvs, tvs = [], []
    for i in range(verts):
        a = 2.0 * math.pi * i / verts
        c, s = math.cos(a), math.sin(a)
        bvs.append(bm.verts.new((r0 * c, r0 * s, -length * 0.5)))
        tvs.append(bm.verts.new((r1 * c, r1 * s,  length * 0.5)))
    for i in range(verts):
        j = (i + 1) % verts
        bm.faces.new([bvs[i], bvs[j], tvs[j], tvs[i]])
    bm.faces.new(tvs)
    bm.faces.new(bvs[::-1])
    bm.normal_update()

    mesh = bpy.data.meshes.new(name)
    obj  = bpy.data.objects.new(name, mesh)
    bpy.context.scene.collection.objects.link(obj)
    bm.to_mesh(mesh); bm.free(); mesh.update()

    rot = Vector((0, 0, 1)).rotation_difference(direction)
    obj.rotation_mode = 'QUATERNION'
    obj.rotation_quaternion = rot
    obj.location = center
    apply_tf(obj)
    return obj

def make_leaf_ear(base, tip, side_dir, length, width, thickness, name="ear"):
    """Build a flat leaf-shaped ear with lateral curl."""
    n = 12
    base_v = Vector(base)
    tip_v = Vector(tip)
    main = (tip_v - base_v).normalized()
    side_v = Vector(side_dir).normalized()
    up = main.cross(side_v).normalized()
    bm = bmesh.new()
    verts = []
    for i in range(n + 1):
        t = i / n
        cx = base_v + main * (length * t)
        w = width * math.sin(math.pi * t) * (1.0 - 0.3 * t)
        v_l = bm.verts.new(cx + side_v * w + up * (thickness * 0.4 * (1 - 2 * abs(t - 0.5))))
        v_r = bm.verts.new(cx - side_v * w - up * (thickness * 0.4 * (1 - 2 * abs(t - 0.5))))
        verts.append((v_l, v_r))
    for i in range(n):
        try: bm.faces.new([verts[i][0], verts[i + 1][0], verts[i + 1][1], verts[i][1]])
        except Exception: pass
    bm.normal_update()
    mesh = bpy.data.meshes.new(name)
    bm.to_mesh(mesh); bm.free(); mesh.update()
    obj = bpy.data.objects.new(name, mesh)
    bpy.context.scene.collection.objects.link(obj)
    return obj


def make_cone(base_center, apex, base_r, verts=5, name="cone"):
    return make_tube(base_center, apex, base_r, 0.0, verts=verts, name=name)


def make_muscle(bone_a, bone_b, bone_radius, lateral_dir,
                t_center=0.5, span=0.6, depth=0.7, width=1.0,
                tilt_into_bone=0.55, name="muscle"):
    """Sausage-shaped surface bulge attached to a bone segment.

    Approximates infinigen's surface_muscle nodegroup for mesh-only output:
    the bulge is rendered as an oriented ellipsoid half-buried in the bone
    so that voxel remesh fuses it into a smooth anatomical bulge.

    bone_a, bone_b : segment endpoints (Vector / 3-tuple)
    bone_radius    : average radius of the underlying tube
    lateral_dir    : direction the bulge protrudes (any vector; orthogonalised against bone)
    t_center       : 0..1 placement along the segment
    span           : longitudinal extent / segment length
    depth          : ellipsoid radius outward from bone, in multiples of bone_radius
    width          : ellipsoid radius tangent to the bone surface, in multiples of bone_radius
    tilt_into_bone : how far the muscle centre sinks toward the bone, in multiples of bone_radius
    """
    a, b = Vector(bone_a), Vector(bone_b)
    bone = b - a
    seg_len = bone.length
    if seg_len < 1e-5:
        return None
    bone_dir = bone / seg_len

    lat = Vector(lateral_dir)
    lat -= lat.dot(bone_dir) * bone_dir
    if lat.length < 1e-5:
        fallback = Vector((1, 0, 0)) if abs(bone_dir.x) < 0.9 else Vector((0, 1, 0))
        lat = fallback - fallback.dot(bone_dir) * bone_dir
    lat = lat.normalized()
    bitan = bone_dir.cross(lat).normalized()

    half_span = seg_len * span * 0.5
    rx = bone_radius * width
    ry = bone_radius * depth
    rz = half_span

    bpy.ops.mesh.primitive_ico_sphere_add(subdivisions=2, radius=1.0, location=(0, 0, 0))
    obj = bpy.context.active_object
    obj.name = name

    center = a + bone * t_center + lat * (bone_radius * tilt_into_bone)
    rot = Matrix((bitan, lat, bone_dir)).transposed().to_4x4()
    scale = Matrix.Diagonal((rx, ry, rz, 1.0))
    obj.matrix_world = Matrix.Translation(center) @ rot @ scale
    apply_tf(obj)
    return obj


def _quadratic_bezier_pts(start, middle, end, n):
    """Sample n points along a quadratic Bezier curve (Vector args)."""
    pts = []
    for i in range(n):
        t = i / max(n - 1, 1)
        u = 1.0 - t
        pts.append(start * (u * u) + middle * (2 * u * t) + end * (t * t))
    return pts


