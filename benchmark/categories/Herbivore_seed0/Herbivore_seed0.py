def _eval_curve(pts, t):
    """Piecewise-linear sample of a (x, y) control polyline (Blender FloatCurve approx)."""
    t = max(pts[0][0], min(t, pts[-1][0]))
    for i in range(len(pts) - 1):
        x0, y0 = pts[i]; x1, y1 = pts[i + 1]
        if t <= x1:
            f = (t - x0) / max(x1 - x0, 1e-9)
            return y0 + (y1 - y0) * f
    return pts[-1][1]


def _rotate_vec(v, axis, theta):
    """Rodrigues rotation of a Vector around a unit-axis Vector by theta radians."""
    axis_n = axis.normalized()
    return (v * math.cos(theta)
            + axis_n.cross(v) * math.sin(theta)
            + axis_n * axis_n.dot(v) * (1.0 - math.cos(theta)))


# Float-curve taper of nodegroup_ridge — head-end thick, tip thin
_HORN_TAPER_PTS = [
    (0.0, 1.0), (0.2, 0.9), (0.3705, 0.7406), (0.55, 0.5938),
    (0.6886, 0.4188), (0.85, 0.1844), (1.0, 0.0),
]


def make_ridged_horn(base_v, fwd_dir, curl_dir, length, rad1, rad2,
                     curl_amp=0.3, depth_of_ridge=0.18, n_segs=60, n_verts=10,
                     name="horn"):
    """Tapered curved horn with 5-period saw-tooth ridge — port of nodegroup_horn + nodegroup_ridge.

    Skeleton: base + fwd*(L*t) + curl*(L*curl_amp*t^2)  (quadratic curl)
    Ring radius: lerp(rad1,rad2,t) * float_curve(t) * (1 + depth*(-1)^(i%5)) * (1 + small_noise)
    """
    base_v = Vector(base_v)
    fwd_dir = Vector(fwd_dir).normalized()
    curl_dir = Vector(curl_dir)
    curl_dir = (curl_dir - curl_dir.dot(fwd_dir) * fwd_dir)
    if curl_dir.length < 1e-5:
        curl_dir = Vector((0, 0, 1)) - Vector((0, 0, 1)).dot(fwd_dir) * fwd_dir
    curl_dir = curl_dir.normalized()

    spine = []
    for k in range(n_segs + 1):
        t = k / n_segs
        spine.append(base_v + fwd_dir * (length * t)
                     + curl_dir * (length * curl_amp * t * t))

    bm = bmesh.new()
    rings = []
    for k in range(n_segs + 1):
        t = k / n_segs
        if k == 0:        tangent = (spine[1] - spine[0]).normalized()
        elif k == n_segs: tangent = (spine[k] - spine[k - 1]).normalized()
        else:             tangent = (spine[k + 1] - spine[k - 1]).normalized()

        up_world = Vector((0, 0, 1))
        if abs(tangent.dot(up_world)) > 0.95:
            up_world = Vector((1, 0, 0))
        side_axis = tangent.cross(up_world).normalized()
        up_axis = side_axis.cross(tangent).normalized()

        r_lerp = rad1 + (rad2 - rad1) * t
        taper = _eval_curve(_HORN_TAPER_PTS, t)
        ridge = 1.0 + depth_of_ridge * ((-1.0) ** (k % 5))
        radius_jitter = 1.0 + float(np.random.normal(0, 0.04))
        r = max(r_lerp * taper * ridge * radius_jitter, 1e-4)

        ring = []
        for v in range(n_verts):
            a = 2.0 * math.pi * v / n_verts
            p = spine[k] + side_axis * (math.cos(a) * r) + up_axis * (math.sin(a) * r)
            ring.append(bm.verts.new(p))
        rings.append(ring)

    for k in range(n_segs):
        for v in range(n_verts):
            v1 = (v + 1) % n_verts
            try:
                bm.faces.new([rings[k][v], rings[k + 1][v], rings[k + 1][v1], rings[k][v1]])
            except Exception:
                pass
    try: bm.faces.new(rings[0][::-1])
    except Exception: pass
    bm.normal_update()

    mesh = bpy.data.meshes.new(name)
    bm.to_mesh(mesh); bm.free(); mesh.update()
    obj = bpy.data.objects.new(name, mesh)
    bpy.context.scene.collection.objects.link(obj)
    return obj


def make_hoof_nurbs(length, width, depth_y, r=2, n=20, m=20, name="hoof"):
    """Cleft-toe NURBS hoof — port of parts/hoof.py Hoof.generate.

    Local frame: BASE (wide ground end) at z=0, TIP (narrow ankle end) at z=length,
    +X = toe direction (cleft is at +X of the cross-section), +Y = lateral.
    """
    r = max(0, int(r))
    if r > m // 3: r = m // 3

    points = []
    for j in range(m):
        theta = 2.0 * math.pi * j / m
        nx = float(np.random.normal(0, 0.01))
        ny = float(np.random.normal(0, 0.01))
        if r >= 1 and (j >= m - r or j <= r):
            points.append((-0.2 * math.cos(theta) + nx, 0.05 * math.sin(theta) + ny))
        elif r >= 1 and (j >= m - 2 * r or j <= 2 * r):
            points.append((math.cos(theta) + nx, 0.2 * math.sin(theta) + ny))
        else:
            points.append((math.cos(theta) + nx, math.sin(theta) + ny))

    ctrls = np.zeros((n, m, 3))
    for i in range(n):
        for j in range(m):
            p = i / (n - 1)
            theta = 2.0 * math.pi * j / m
            scale_p = 1.0 - 0.2 * p
            transform_p = -0.6 * p
            down_p = 0.4 * p * math.cos(theta)
            ctrls[i, j, 0] = (scale_p * points[j][0] + transform_p) * width
            ctrls[i, j, 1] = (scale_p * points[j][1]) * depth_y
            ctrls[i, j, 2] = (p + down_p) * length

    pts, nu, nv = _eval_nurbs_surface(ctrls, face_size=0.005, cyclic_v=True)
    verts = pts.reshape(-1, 3).tolist()
    faces = []
    for i in range(nu - 1):
        for j in range(nv):
            jn = (j + 1) % nv
            faces.append((i * nv + j, i * nv + jn, (i + 1) * nv + jn, (i + 1) * nv + j))

    # Cap base + tip with center-fan to ensure a closed mesh for voxel remesh
    base_c = ctrls[0].mean(axis=0)
    base_c_idx = len(verts)
    verts.append([float(base_c[0]), float(base_c[1]), float(base_c[2])])
    for j in range(nv):
        jn = (j + 1) % nv
        faces.append((j, jn, base_c_idx))

    tip_c = ctrls[-1].mean(axis=0)
    tip_c_idx = len(verts)
    verts.append([float(tip_c[0]), float(tip_c[1]), float(tip_c[2])])
    base_offset = (nu - 1) * nv
    for j in range(nv):
        jn = (j + 1) % nv
        faces.append((base_offset + jn, base_offset + j, tip_c_idx))

    mesh = bpy.data.meshes.new(name)
    mesh.from_pydata(verts, [], faces)
    mesh.update()
    obj = bpy.data.objects.new(name, mesh)
    bpy.context.scene.collection.objects.link(obj)

    activate(obj)
    bpy.ops.object.mode_set(mode='EDIT')
    bpy.ops.mesh.select_all(action='SELECT')
    bpy.ops.mesh.remove_doubles(threshold=1e-4)
    bpy.ops.mesh.normals_make_consistent(inside=False)
    bpy.ops.object.mode_set(mode='OBJECT')
    bpy.ops.object.shade_smooth()
    return obj


def make_curled_ear(base_pos, fwd_dir, side_axis, length, peak_width, bowl_depth,
                    curl_back_dir=(-1.0, 0.0, 0.0),
                    thickness=0.01, curl_deg=49.0, n_long=24, n_cross=9, name="ear"):
    """Curled cat-ear with bowl cross-section + Solidify — port of nodegroup_cat_ear.

    base_pos:      3D anchor on head surface
    fwd_dir:       initial outward direction (ear curls toward curl_back_dir)
    side_axis:     lateral axis of the ear (its width spans ±side_axis)
    length:        total ear length along the curl arc
    peak_width:    max ear width at FloatCurve peak (~32% along length)
    bowl_depth:    cross-section concavity (dips toward bowl_axis)
    curl_back_dir: world direction the tip should curl toward (default -X = animal back);
                   curl axis is fwd_dir × curl_back_dir so both ears bend the same way
    """
    base_pos = Vector(base_pos)
    fwd_dir = Vector(fwd_dir).normalized()
    side_axis = Vector(side_axis)
    side_axis = side_axis - side_axis.dot(fwd_dir) * fwd_dir
    if side_axis.length < 1e-4:
        side_axis = Vector((0, 1, 0))
    side_axis = side_axis.normalized()

    back_world = Vector(curl_back_dir).normalized()
    curl_axis = fwd_dir.cross(back_world)
    if curl_axis.length < 1e-4:
        curl_axis = side_axis
    curl_axis = curl_axis.normalized()

    fc_pts = [(0.0, 0.0), (0.3236, 0.98), (0.7462, 0.63), (1.0, 0.0)]

    curl_rad = math.radians(curl_deg)

    positions = [base_pos.copy()]
    tangents = [fwd_dir.copy()]
    ds = length / max(n_long - 1, 1)
    for k in range(1, n_long):
        theta = (k / (n_long - 1)) * curl_rad
        t_k = _rotate_vec(fwd_dir, curl_axis, theta)
        tangents.append(t_k)
        positions.append(positions[-1] + (tangents[-2] + t_k) * 0.5 * ds)

    bm = bmesh.new()
    grid = []
    for i in range(n_long):
        u = i / (n_long - 1)
        pos = positions[i]
        tan = tangents[i]
        # bowl axis = curl_back_dir projected away from tangent → middle of cross-section
        # dips toward animal back, leaving the concave side facing animal front
        bowl_axis = back_world - back_world.dot(tan) * tan
        if bowl_axis.length < 1e-5:
            bowl_axis = curl_axis.cross(tan)
        bowl_axis = bowl_axis.normalized()
        w = peak_width * _eval_curve(fc_pts, u)
        row = []
        for j in range(n_cross):
            v = j / (n_cross - 1)
            lat = (v - 0.5) * w
            bowl = bowl_depth * 4.0 * v * (1.0 - v)
            p = pos + side_axis * lat + bowl_axis * bowl
            row.append(bm.verts.new(p))
        grid.append(row)

    for i in range(n_long - 1):
        for j in range(n_cross - 1):
            try:
                bm.faces.new([grid[i][j], grid[i + 1][j], grid[i + 1][j + 1], grid[i][j + 1]])
            except Exception:
                pass
    bm.normal_update()

    mesh = bpy.data.meshes.new(name)
    bm.to_mesh(mesh); bm.free(); mesh.update()
    obj = bpy.data.objects.new(name, mesh)
    bpy.context.scene.collection.objects.link(obj)

    activate(obj)
    sol = obj.modifiers.new("solidify", 'SOLIDIFY')
    sol.thickness = thickness
    sol.offset = 0.0
    try:
        bpy.ops.object.modifier_apply(modifier=sol.name)
    except Exception:
        pass
    return obj


def make_eyelid(eye_center, eye_radius, outward, up_dir,
                tear_duct_coord=(0.0, -1.2, -0.2),
                peak_coord=(1.2, 0.4, -1.7),
                eyelid_end_coord=(0.0, 1.2, 0.31),
                start_rad_pct=0.5, end_rad_pct=0.5,
                bulge=0.35, n_long=18, n_cross=8, name="eyelid"):
    """Eyelid as curved tube wrapping over the eyeball — port of nodegroup_eyelid.

    Skeleton = QuadraticBezier(TearDuct, Peak, EyelidEnd) on a sphere of eye_radius
    around eye_center, expressed in a local frame where +X = outward (gaze direction),
    +Y = front-back of eye, +Z = up. Cross-section = circle scaled by smooth taper.
    """
    eye_center = Vector(eye_center)
    outward = Vector(outward).normalized()
    up_dir = Vector(up_dir).normalized()
    front_back = up_dir.cross(outward)
    if front_back.length < 1e-4:
        front_back = Vector((1, 0, 0)) - Vector((1, 0, 0)).dot(outward) * outward
    front_back = front_back.normalized()
    up_perp = outward.cross(front_back).normalized()

    def _local_to_world(c):
        return (eye_center
                + outward * (eye_radius * c[0])
                + front_back * (eye_radius * c[1])
                + up_perp * (eye_radius * c[2]))

    start = _local_to_world(tear_duct_coord)
    middle = _local_to_world(peak_coord)
    end = _local_to_world(eyelid_end_coord)
    spine = _quadratic_bezier_pts(start, middle, end, n_long)

    bm = bmesh.new()
    rings = []
    for k in range(n_long):
        t = k / max(n_long - 1, 1)
        if k == 0:           tan = (spine[1] - spine[0]).normalized()
        elif k == n_long - 1: tan = (spine[k] - spine[k - 1]).normalized()
        else:                tan = (spine[k + 1] - spine[k - 1]).normalized()

        cand = outward
        side_a = tan.cross(cand)
        if side_a.length < 1e-4:
            side_a = tan.cross(up_perp)
        side_a = side_a.normalized()
        side_b = side_a.cross(tan).normalized()

        r = eye_radius * (start_rad_pct + (end_rad_pct - start_rad_pct) * t)
        r *= 1.0 + bulge * 4.0 * t * (1.0 - t)  # peaks mid-curve

        ring = []
        for v in range(n_cross):
            a = 2.0 * math.pi * v / n_cross
            p = spine[k] + side_a * (r * math.cos(a)) + side_b * (r * math.sin(a))
            ring.append(bm.verts.new(p))
        rings.append(ring)

    for k in range(n_long - 1):
        for v in range(n_cross):
            v1 = (v + 1) % n_cross
            try:
                bm.faces.new([rings[k][v], rings[k + 1][v], rings[k + 1][v1], rings[k][v1]])
            except Exception:
                pass
    try: bm.faces.new(rings[0][::-1])
    except Exception: pass
    try: bm.faces.new(rings[-1])
    except Exception: pass
    bm.normal_update()

    mesh = bpy.data.meshes.new(name)
    bm.to_mesh(mesh); bm.free(); mesh.update()
    obj = bpy.data.objects.new(name, mesh)
    bpy.context.scene.collection.objects.link(obj)
    return obj


def make_cat_nose(center, outward, up_dir, length, width, height,
                  nostril_size=0.025, name="nose"):
    """Subdivided cube nose with 2 frontal nostril dimples — approximation of nodegroup_cat_nose.

    Boolean nostrils get wiped by voxel remesh, so the dimples are carved as
    inward vertex displacement instead, sized large enough to survive remesh.
    """
    center = Vector(center)
    outward = Vector(outward).normalized()
    up_dir = Vector(up_dir).normalized()
    side_v = up_dir.cross(outward)
    if side_v.length < 1e-4:
        side_v = Vector((0, 1, 0))
    side_v = side_v.normalized()
    up_perp = outward.cross(side_v).normalized()

    bpy.ops.mesh.primitive_cube_add(size=1.0, location=(0, 0, 0))
    obj = bpy.context.active_object
    obj.name = name
    obj.scale = (length, width, height)
    apply_tf(obj)
    apply_mod(obj, 'SUBSURF', levels=3)

    me = obj.data
    bm = bmesh.new()
    bm.from_mesh(me)
    nostril_centers = [
        Vector((length * 0.85,  width * 0.45, height * 0.10)),
        Vector((length * 0.85, -width * 0.45, height * 0.10)),
    ]
    for nc in nostril_centers:
        for v in bm.verts:
            d = (v.co - nc).length
            if d < nostril_size:
                falloff = 1.0 - (d / nostril_size)
                v.co += Vector((-nostril_size * 0.55 * falloff, 0.0, 0.0))
    bm.to_mesh(me)
    bm.free()
    me.update()

    rot = Matrix((outward, side_v, up_perp)).transposed().to_4x4()
    obj.matrix_world = Matrix.Translation(center) @ rot
    apply_tf(obj)
    return obj


def make_jaw(base_v, tip_v, rad1, rad2, lateral_aspect=1.7,
             n_segs=24, n_verts=10, name="jaw"):
    """Tapered jaw with oval cross-section + width FloatCurve — port of nodegroup_carnivore_jaw
    (canine/incisor branches skipped, since herbivore_genome zeroes Canine Length)."""
    base_v = Vector(base_v)
    tip_v = Vector(tip_v)
    spine_dir = (tip_v - base_v)
    seg_len = spine_dir.length
    if seg_len < 1e-5:
        return None
    spine_dir = spine_dir / seg_len

    up_world = Vector((0, 0, 1))
    side = spine_dir.cross(up_world)
    if side.length < 0.4:
        side = Vector((0, 1, 0)) - Vector((0, 1, 0)).dot(spine_dir) * spine_dir
    side = side.normalized()
    up_local = side.cross(spine_dir).normalized()

    bm = bmesh.new()
    rings = []
    for k in range(n_segs + 1):
        t = k / n_segs
        center = base_v + spine_dir * (seg_len * t)
        r_lerp = rad1 + (rad2 - rad1) * t
        # Width FloatCurve + smooth-taper bulge (fullness ~2.6 → mid-bulge of ~30%)
        width_mod = _eval_curve(_JAW_WIDTH_PTS, t) * (1.0 + 0.30 * 4.0 * t * (1.0 - t))
        rx = r_lerp * width_mod * lateral_aspect
        ry = r_lerp * width_mod
        ring = []
        for v in range(n_verts):
            a = 2.0 * math.pi * v / n_verts
            p = center + side * (rx * math.cos(a)) + up_local * (ry * math.sin(a))
            ring.append(bm.verts.new(p))
        rings.append(ring)

    for k in range(n_segs):
        for v in range(n_verts):
            v1 = (v + 1) % n_verts
            try:
                bm.faces.new([rings[k][v], rings[k + 1][v], rings[k + 1][v1], rings[k][v1]])
            except Exception:
                pass
    try: bm.faces.new(rings[0][::-1])
    except Exception: pass
    try: bm.faces.new(rings[-1])
    except Exception: pass
    bm.normal_update()

    mesh = bpy.data.meshes.new(name)
    bm.to_mesh(mesh); bm.free(); mesh.update()
    obj = bpy.data.objects.new(name, mesh)
    bpy.context.scene.collection.objects.link(obj)
    return obj


# ── NURBS body construction ─────────────────────────────────────────────────

def make_nurbs_body(body_seed):
    """Create herbivore body from blended NURBS control point data.

    Loads 4 reference body shapes (cow, goat, llama, giraffe) and blends them
    with Dirichlet-weighted combination (matching infinigen's NurbsBody).
    Creates mesh from the (9, 8, 3) control point grid with SUBSURF smoothing.

    Returns: (obj, skeleton, radii, handles)
      obj:      Blender mesh object (SUBSURF applied)
      skeleton: (9, 3) ring centroids
      radii:    (9,) mean ring radii
      handles:  (9, 8, 3) final blended+noised control points
    """
    rng = np.random.RandomState(body_seed)

    # Dirichlet blending (matching infinigen temperature dict)
    # cow=0.2, goat=0.2, llama=0.1, giraffe=0.02
    temp = np.array([0.2, 0.2, 0.1, 0.02])
    weights = rng.dirichlet(temp)
    handles = sum(w * s for w, s in zip(weights, _NURBS_SHAPES))  # (9, 8, 3)

    # NurbsPart-style perturbation: thetas / proportions / rads / profiles each independent
    params = _decompose_params(handles)
    # NurbsBody noise: var=1.0 matching infinigen NurbsBody(var=1, ...)
    params = _perturb_params(params, rng, var=1.0)
    handles = _recompose_handles(params)

    skeleton = handles.mean(axis=1)
    radii = np.linalg.norm(
        handles - skeleton[:, np.newaxis, :], axis=2
    ).mean(axis=1)

    pts, nu, nv = _eval_nurbs_surface(handles, face_size=0.04, cyclic_v=True)
    verts = pts.reshape(-1, 3)
    faces = []
    for i in range(nu - 1):
        for j in range(nv):
            jn = (j + 1) % nv
            faces.append((i * nv + j, i * nv + jn, (i + 1) * nv + jn, (i + 1) * nv + j))
    mesh = bpy.data.meshes.new("nurbs_body")
    mesh.from_pydata(verts.tolist(), [], faces)
    mesh.update()
    obj = bpy.data.objects.new("nurbs_body", mesh)
    bpy.context.scene.collection.objects.link(obj)

    activate(obj)
    bpy.ops.object.mode_set(mode='EDIT')
    bpy.ops.mesh.select_all(action='SELECT')
    bpy.ops.mesh.remove_doubles(threshold=1e-4)
    bpy.ops.mesh.normals_make_consistent(inside=False)
    bpy.ops.object.mode_set(mode='OBJECT')
    bpy.ops.object.shade_smooth()

    return obj, skeleton, radii, handles

# ── Rotation helper ──────────────────────────────────────────────────────────

def _rot_matrix(vec_from, vec_to):
    """3x3 rotation matrix mapping unit vector vec_from to vec_to (Rodrigues)."""
    a = np.asarray(vec_from, dtype=float)
    b = np.asarray(vec_to, dtype=float)
    a = a / np.linalg.norm(a)
    b = b / np.linalg.norm(b)
    v = np.cross(a, b)
    c = float(np.dot(a, b))
    if c > 0.9999:
        return np.eye(3)
    if c < -0.9999:
        p = np.array([1, 0, 0]) if abs(a[0]) < 0.9 else np.array([0, 1, 0])
        p = p - np.dot(p, a) * a
        p /= np.linalg.norm(p)
        return 2.0 * np.outer(p, p) - np.eye(3)
    s = np.linalg.norm(v)
    vx = np.array([[0, -v[2], v[1]], [v[2], 0, -v[0]], [-v[1], v[0], 0]])
    return np.eye(3) + vx + vx @ vx * ((1 - c) / (s * s))

# ── NURBS head construction ──────────────────────────────────────────────────

def make_nurbs_head(head_seed, neck_tip_pos, neck_dir_vec):
    """Create herbivore head from blended NURBS control point data.

    Blends 4 reference head shapes (cow, goat, llama, giraffe) with Dirichlet
    weights (matching infinigen's NurbsHead with var=0.5).
    Head is rotated to align with neck_dir_vec and positioned at neck_tip_pos.

    Returns: (obj, skeleton, radii, handles) in world coordinates.
      obj:      Blender mesh object (SUBSURF applied)
      skeleton: (9, 3) ring centroids
      radii:    (9,) mean ring radii
      handles:  (9, 12, 3) final blended+noised control points
    """
    rng = np.random.RandomState(head_seed)
    neck_tip_pos = np.asarray(neck_tip_pos, dtype=float)
    neck_dir_vec = np.asarray(neck_dir_vec, dtype=float)
    neck_n = neck_dir_vec / max(np.linalg.norm(neck_dir_vec), 1e-8)

    # Dirichlet blending — head temp_dict only matches "body_herbivore" prefix
    # so all 4 head shapes default to alpha=0.2 (uniform sampling)
    temp = np.array([0.2, 0.2, 0.2, 0.2])
    weights = rng.dirichlet(temp)
    handles = sum(w * s for w, s in zip(weights, _NURBS_HEAD_SHAPES))  # (9, 12, 3)

    # NurbsHead noise: var=0.5 matching infinigen NurbsHead(var=0.5, ...)
    params = _decompose_params(handles)
    params = _perturb_params(params, rng, var=0.5)
    handles = _recompose_handles(params)

    skeleton = handles.mean(axis=1)

    # Rotation: align head's forward axis (+X) with neck direction,
    # then apply ~15° downward pitch (Joint rest=(0,20,0) in infinigen)
    head_fwd = skeleton[-1] - skeleton[0]
    # Compute pitched target: rotate neck_n downward around lateral axis
    lateral = np.cross(neck_n, [0, 0, 1])
    lat_norm = np.linalg.norm(lateral)
    if lat_norm > 1e-6:
        lateral /= lat_norm
        pitch = np.radians(-20)  # matches infinigen genome.attach(head, body, joint=Joint(rest=(0, 20, 0)))
        cp, sp = np.cos(pitch), np.sin(pitch)
        K = np.array([[0, -lateral[2], lateral[1]],
                       [lateral[2], 0, -lateral[0]],
                       [-lateral[1], lateral[0], 0]])
        target_dir = (np.eye(3) + sp * K + (1 - cp) * K @ K) @ neck_n
    else:
        target_dir = neck_n
    rot = _rot_matrix(head_fwd, target_dir)

    # Rotate all handles around back-of-head pivot
    pivot = skeleton[0].copy()
    for i in range(9):
        for j in range(12):
            handles[i, j] = rot @ (handles[i, j] - pivot) + pivot
    skeleton = handles.mean(axis=1)

    # Position: back of head overlaps with neck tip (2cm overlap into neck)
    overlap = 0.02
    translation = neck_tip_pos - skeleton[0] - neck_n * overlap
    handles += translation
    skeleton += translation

    # Radii
    radii = np.linalg.norm(
        handles - skeleton[:, np.newaxis, :], axis=2
    ).mean(axis=1)

    pts, nu, nv = _eval_nurbs_surface(handles, face_size=0.04, cyclic_v=True)
    verts = pts.reshape(-1, 3)
    faces = []
    for i in range(nu - 1):
        for j in range(nv):
            jn = (j + 1) % nv
            faces.append((i * nv + j, i * nv + jn, (i + 1) * nv + jn, (i + 1) * nv + j))
    mesh = bpy.data.meshes.new("nurbs_head")
    mesh.from_pydata(verts.tolist(), [], faces)
    mesh.update()
    obj = bpy.data.objects.new("nurbs_head", mesh)
    bpy.context.scene.collection.objects.link(obj)

    activate(obj)
    bpy.ops.object.mode_set(mode='EDIT')
    bpy.ops.mesh.select_all(action='SELECT')
    bpy.ops.mesh.remove_doubles(threshold=1e-4)
    bpy.ops.mesh.normals_make_consistent(inside=False)
    bpy.ops.object.mode_set(mode='OBJECT')
    bpy.ops.object.shade_smooth()

    return obj, skeleton, radii, handles

def head_pt(skeleton, handles, t, prof_idx, r=1.0):
    """Get interpolated point on head surface.

    t: 0-1 along head skeleton (0=back, 1=snout tip)
    prof_idx: continuous profile index (0-11).
              Left (+Y) side: 0-5 (0=lower-left, 2=left, 5=top).
              Right (-Y) side: 6-11 (8=right, 10=lower-right, 11=bottom).
              Mirror formula: right_idx = 10.0 - left_idx  (for left_idx in [0,5])
    r: 0-1 from skeleton center (0) to surface (1)

    Returns: numpy array (3,)
    """
    n = len(skeleton)
    rf = t * (n - 1)
    ri = max(0, min(int(rf), n - 2))
    f = rf - ri
    c = skeleton[ri] * (1 - f) + skeleton[ri + 1] * f
    pts = handles[ri] * (1 - f) + handles[ri + 1] * f  # (12, 3)
    pi = int(prof_idx) % 12
    pf = prof_idx - int(prof_idx)
    if pf < 0:
        pf = 0
    pj = (pi + 1) % 12
    s = pts[pi] * (1 - pf) + pts[pj] * pf
    return c + (s - c) * r

# ── Main build ───────────────────────────────────────────────────────────────

def build_herbivore(seed=0):
    np.random.seed(seed)
    random.seed(seed)
    clear_scene()
    parts = []

    # ── Overall scale (herbivore_genome length noise) ─────────────────────────
    len_scale = float(np.clip(N(1.0, 0.10) * N(1.0, 0.10), 0.75, 1.30))

    # ── Lateral splay: clip_gaussian(130,7,90,130)/180 ────────────────────────
    splay_frac = clip_gaussian(130, 7, 90, 130) / 180   # ≈ 0.72

    # ── Leg lengths: (1.8,0.1,0.05)*N(1,(0.1,0.05,0.05),3) ──────────────────
    leg_len_norm  = 1.8  * float(np.random.normal(1.0, 0.10))
    leg_rad1_norm = 0.10 * float(np.random.normal(1.0, 0.05))  # consumed for RNG match
    leg_rad2_norm = 0.05 * float(np.random.normal(1.0, 0.05))  # consumed for RNG match

    leg_scale = 0.472 * len_scale   # maps normalized 1.8 → world ≈ 0.85m (infinigen QuadrupedBackLeg default)
    # 15% giraffe/long-leg branch — matches herbivore_genome U()<0.15 lenscale*=U(1,1.3)
    leg_lenscale = U(1.0, 1.3) if U() < 0.15 else 1.0
    total_leg = leg_len_norm * leg_scale * leg_lenscale

    # ── NURBS body (separate RNG preserves main sequence) ────────────────────
    body, skeleton, radii, handles = make_nurbs_body(seed + 1000)

    # Center body: midpoint of ring 2 (hips) and ring 5 (shoulders) at x=0
    center_x = (skeleton[2, 0] + skeleton[5, 0]) / 2.0

    # Vertical position: body bottom at total_leg * 0.75
    body_bottoms = [skeleton[i, 2] - radii[i] for i in range(1, 8)]
    body_bottom = min(body_bottoms)
    target_bottom_z = total_leg * 0.75
    z_offset = target_bottom_z - body_bottom

    body.location = Vector((-center_x, 0, z_offset))
    apply_tf(body)

    # Update arrays to world coordinates
    skeleton[:, 0] -= center_x
    skeleton[:, 2] += z_offset
    handles[:, :, 0] -= center_x
    handles[:, :, 2] += z_offset

    parts.append(body)

    # ── Leg attachment positions from skeleton ───────────────────────────────
    back_x = skeleton[2, 0]     # hip ring X
    front_x = skeleton[5, 0]   # shoulder ring X

    # Leg Y: body side radius at hip ring × splay
    # Profile points 1 and 5 are the side (left/right) points
    back_side_y = float(np.abs(handles[2, [1, 5], 1] - skeleton[2, 1]).mean())
    leg_y = back_side_y * splay_frac

    # ── Back leg angles: S-shape (28, -84, 70) ──────────────────────────────
    # angles match the original QuadrupedBackLeg.angles_deg = (30, -100, 81) — sharper S-curve than my earlier (28,-84,70)
    back_a1, back_a2, back_a3 = math.radians(30), math.radians(-100), math.radians(81)
    cum_a1 = back_a1
    cum_a2 = cum_a1 + back_a2   # -56°
    cum_a3 = cum_a2 + back_a3   # +14°
    d0 = Vector((math.sin(cum_a1), 0, -math.cos(cum_a1)))
    d1 = Vector((math.sin(cum_a2), 0, -math.cos(cum_a2)))
    d2 = Vector((math.sin(cum_a3), 0, -math.cos(cum_a3)))
    back_z_per_unit = abs(d0.z) + abs(d1.z) + abs(d2.z)

    # ── Front leg angles: gentle S-shape (-12, 38, -26) ──────────────────────
    front_a1, front_a2, front_a3 = math.radians(-12), math.radians(38), math.radians(-26)
    cum_b1 = front_a1
    cum_b2 = cum_b1 + front_a2
    cum_b3 = cum_b2 + front_a3
    e0 = Vector((math.sin(cum_b1), 0, -math.cos(cum_b1)))
    e1 = Vector((math.sin(cum_b2), 0, -math.cos(cum_b2)))
    e2 = Vector((math.sin(cum_b3), 0, -math.cos(cum_b3)))
    front_z_per_unit = abs(e0.z) + abs(e1.z) + abs(e2.z)

    # Leg start Z: lower portion of body at hip/shoulder rings
    intended_back_z  = skeleton[2, 2] - radii[2] * 0.35
    intended_front_z = skeleton[5, 2] - radii[5] * 0.35
    back_seg  = intended_back_z  / max(back_z_per_unit,  0.01)
    front_seg = intended_front_z / max(front_z_per_unit, 0.01)

    # ── Head (NURBS-based, from neck tip) ─────────────────────────────────────
    # Neck is part of the NURBS body (rings 6-7-8); head uses separate NURBS data
    neck_tip_v = skeleton[7].copy()
    neck_dir_v = skeleton[7] - skeleton[6]
    nd_len = float(np.linalg.norm(neck_dir_v))
    if nd_len > 1e-5:
        neck_dir_v = neck_dir_v / nd_len
    else:
        neck_dir_v = np.array([1.0, 0.0, 0.0])

    head_obj, head_skel, head_radii, head_hdl = make_nurbs_head(
        seed + 2000, neck_tip_v, neck_dir_v
    )
    parts.append(head_obj)

    # Head dimensions derived from NURBS shape (for feature sizing)
    head_len = float(np.linalg.norm(head_skel[-1] - head_skel[0]))
    head_w = float(np.max(np.abs(head_hdl[:, :, 1] - head_skel[:, np.newaxis, 1])))
    head_h = float(np.max(np.abs(head_hdl[:, :, 2] - head_skel[:, np.newaxis, 2])))

    # ── Eyes (positioned on lateral head surface via NURBS interpolation) ─────
    eye_r  = max(float(np.random.normal(0.035, 0.008)), 0.028)
    eye_t  = U(0.34, 0.45)
    eye_sp = U(80, 140) / 180
    eye_ht = U(0.7, 0.9)
    # Map splay (0.44-0.78) to left-side profile index (~1.5-3.5)
    eye_prof = 1.0 + (eye_sp - 0.44) * 5.0
    eye_prof = clip(eye_prof, 1.0, 4.0)
    # Eyelid radii are scaled up so the lid tube survives voxel_remesh @ 0.025
    lid_radius_pct = 0.6
    for side in [-1, 1]:
        pi = eye_prof if side > 0 else 10.0 - eye_prof
        ep = head_pt(head_skel, head_hdl, eye_t, pi, r=0.82)
        parts.append(make_ellipsoid(ep, eye_r, eye_r * 0.80, eye_r,
                                    subdivisions=2, name=f"eye_{side}"))
        outward = np.array(ep) - np.array(head_skel.mean(axis=0))
        outward[0] *= 0.3
        out_n = np.linalg.norm(outward)
        if out_n <= 1e-6:
            continue
        outward = outward / out_n
        iris_pos = np.array(ep) + outward * eye_r * 0.65
        parts.append(make_ellipsoid(tuple(iris_pos), eye_r * 0.45, eye_r * 0.45,
                                    eye_r * 0.45, subdivisions=2, name=f"iris_{side}"))

        outward_v = Vector(outward.tolist())
        up_v = Vector((0.0, 0.0, 1.0))
        # Mirror the eyelid bezier across the side so the tear duct ends up at the
        # animal's nose-side (forward) for both eyes
        flip = -1.0 if side > 0 else 1.0
        upper = make_eyelid(ep, eye_r, outward_v, up_v,
                            tear_duct_coord=(0.0, flip * -1.2, -0.2),
                            peak_coord=(1.2, flip * 0.4, -1.7),
                            eyelid_end_coord=(0.0, flip * 1.2, 0.31),
                            start_rad_pct=lid_radius_pct, end_rad_pct=lid_radius_pct,
                            n_long=18, n_cross=8, name=f"eyelid_top_{side}")
        if upper: parts.append(upper)
        lower = make_eyelid(ep, eye_r, outward_v, up_v,
                            tear_duct_coord=(0.0, flip * -1.5, -0.2),
                            peak_coord=(1.2, flip * -0.2, 1.8),
                            eyelid_end_coord=(0.0, flip * 1.5, 0.3),
                            start_rad_pct=lid_radius_pct, end_rad_pct=lid_radius_pct,
                            n_long=18, n_cross=8, name=f"eyelid_bot_{side}")
        if lower: parts.append(lower)

    # ── Jaw (CarnivoreJaw attached under head — was missing) ─────────────────
    jaw_len = 0.6 * head_len * float(np.random.normal(1.0, 0.05))
    jaw_r1 = 0.12 * float(np.random.normal(1.0, 0.05))
    jaw_r2 = 0.08 * float(np.random.normal(1.0, 0.05))
    jaw_t = 0.25 * float(np.random.normal(1.0, 0.1))
    jaw_base = head_pt(head_skel, head_hdl, jaw_t, 11.0, r=0.95)
    jaw_dir = Vector((1.0, 0.0, -0.15)).normalized()
    rot_y = math.radians(10.0 * float(np.random.normal(1.0, 0.1)))
    jaw_dir = Vector((
        math.cos(rot_y) * jaw_dir.x + math.sin(rot_y) * jaw_dir.z,
        jaw_dir.y,
        -math.sin(rot_y) * jaw_dir.x + math.cos(rot_y) * jaw_dir.z,
    )).normalized()
    jaw_tip = Vector(jaw_base) + jaw_dir * jaw_len
    j = make_jaw(jaw_base, tuple(jaw_tip), jaw_r1, jaw_r2,
                 lateral_aspect=1.7, n_segs=24, n_verts=10, name="jaw")
    if j: parts.append(j)

    # ── Nose (CatNose, 70% chance) ────────────────────────────────────────────
    if U() < 0.7:
        # Nose at top of muzzle tip (splay=1.0 → profile pt 5 = top)
        np_pos = head_pt(head_skel, head_hdl, 0.90, 5.0, r=0.55)
        # Forward direction = head skeleton tangent at the muzzle tip
        nose_fwd = head_skel[-1] - head_skel[-2]
        nose_fwd_v = Vector(nose_fwd.tolist())
        if nose_fwd_v.length < 1e-5:
            nose_fwd_v = Vector((1.0, 0.0, 0.0))
        nose_fwd_v = nose_fwd_v.normalized()
        nose = make_cat_nose(np_pos, nose_fwd_v, Vector((0.0, 0.0, 1.0)),
                             length=head_len * 0.18, width=head_w * 0.55,
                             height=head_h * 0.32,
                             nostril_size=max(head_w * 0.18, 0.025),
                             name="nose")
        if nose: parts.append(nose)

    # ── Ears (large, leaf-shaped, on upper-lateral head surface) ────────────────
    ear_t    = U(0.15, eye_t - 0.07)
    ear_sp   = float(np.random.normal(125, 15)) / 180
    base_ear = np.array([0.25, 0.10, 0.0]) * np.random.normal(1.0, [0.10, 0.05, 0.05])
    base_ear *= np.random.normal(1.2, 0.10, 3)
    ear_length = float(base_ear[0])   # matches infinigen ear_fac.params["length_rad1_rad2"][0]
    ear_rad    = float(base_ear[1])
    ear_rot = np.array([0, -10, -23]) * np.random.normal(1, 0.10, 3)
    # Map ear splay (~0.69) to profile index (~3.5 upper-lateral, closer to top)
    ear_prof = 3.5 + (ear_sp - 0.55) * 2.0
    ear_prof = clip(ear_prof, 3.0, 4.5)
    # CatEar Curl Deg = 49° in infinigen, but applied as 3-axis Euler in polar_bezier;
    # my arc-based implementation rotates the tangent uniformly, so 49° looks dramatically
    # over-curled in the render. Halving (~25°) better matches the apparent curl of the original.
    ear_curl_deg = 25.0 * float(np.random.normal(1.0, 0.2))

    for side in [-1, 1]:
        pi = ear_prof if side > 0 else 10.0 - ear_prof
        eb = head_pt(head_skel, head_hdl, ear_t, pi, r=0.88)
        ear_up   = math.radians(-ear_rot[1])
        ear_out  = math.radians(-ear_rot[2])
        # Stronger outward splay for visibility (0.30 → 0.55)
        ear_dir  = Vector((
            math.sin(ear_up) * 0.5,
            side * math.sin(abs(ear_out)) * 0.55,
            math.cos(ear_up)
        )).normalized()
        side_axis = Vector((0, side, 0))
        ear = make_curled_ear(eb, ear_dir, side_axis,
                              length=ear_length,
                              peak_width=ear_rad,   # matches infinigen rad1 (FloatCurve peaks at 0.98 of rad1)
                              bowl_depth=ear_length * 0.18,
                              thickness=0.012,
                              curl_deg=ear_curl_deg,
                              n_long=24, n_cross=9,
                              name=f"ear_{side}")
        if ear:
            parts.append(ear)

    # ── Horns (70% chance, positioned on upper head surface) ──────────────────
    if U() < 0.7:
        horn_len = 0.22 * U(0.15, 1.5) * len_scale
        horn_r1  = max(0.05 * U(0.15, 1.3), 0.015)
        horn_r2  = max(0.018 * U(0.15, 1.3), 0.007)
        horn_t   = U(0.25, ear_t)
        horn_sp  = U(ear_sp + 20.0 / 180, 130.0 / 180)
        horn_rx  = U(-40, 0)
        horn_rz  = float(np.random.normal(120, 10))
        # Map horn splay (~0.72-0.80) to profile index (~3.5 upper-lateral/top)
        horn_prof = 3.5 + (horn_sp - 0.65) * 2.5
        horn_prof = clip(horn_prof, 3.0, 4.5)
        horn_curve = U(0.15, 0.5)
        horn_depth = U(0.12, 0.22)
        for side in [-1, 1]:
            pi = horn_prof if side > 0 else 10.0 - horn_prof
            hb = head_pt(head_skel, head_hdl, horn_t, pi, r=0.50)
            rx = math.radians(horn_rx)
            rz = math.radians(side * horn_rz)
            horn_dir = Vector((
                math.sin(rx) * math.sin(rz),
                -math.sin(rx) * math.cos(rz),
                math.cos(rx)
            )).normalized()
            curl_dir = Vector((-side * horn_dir.y, side * horn_dir.x, 0)).normalized()
            h = make_ridged_horn(hb, horn_dir, curl_dir, horn_len, horn_r1, horn_r2,
                                 curl_amp=horn_curve, depth_of_ridge=horn_depth,
                                 n_segs=60, n_verts=10, name=f"horn_{side}")
            if h: parts.append(h)

    # ── Leg radii ────────────────────────────────────────────────────────────
    _rn = lambda: float(np.random.normal(1.0, 0.08))

    thigh_r  = max(0.100 * _rn() * leg_scale * 1.72, 0.115)
    thigh_r2 = max(0.072 * _rn() * leg_scale * 1.70, 0.096)
    calf_r   = max(0.054 * _rn() * leg_scale * 1.72, 0.082)
    calf_r2  = max(0.036 * _rn() * leg_scale * 1.78, 0.074)
    ankle_r  = max(0.030 * _rn() * leg_scale * 1.88, 0.066)

    shldr_r  = max(0.098 * _rn() * leg_scale * 1.72, 0.112)
    shldr_r2 = max(0.070 * _rn() * leg_scale * 1.70, 0.092)
    elbow_r  = max(0.052 * _rn() * leg_scale * 1.72, 0.078)
    elbow_r2 = max(0.034 * _rn() * leg_scale * 1.78, 0.066)
    fore_r   = max(0.028 * _rn() * leg_scale * 1.88, 0.060)
    fore_r2  = max(0.024 * _rn() * leg_scale * 1.88, 0.054)

    # ── Hooves ───────────────────────────────────────────────────────────────
    hoof_sx = max(0.062 * float(np.random.normal(1.0, 0.06)) * leg_scale * 1.55, 0.065)
    hoof_sz = max(0.052 * float(np.random.normal(1.0, 0.06)) * leg_scale * 1.55, 0.055)

    for side in [-1, 1]:
        sy = side * leg_y

        # ── Back leg: thigh → calf → ankle ───────────────────────────────────
        blt = Vector((back_x, sy, intended_back_z))
        blk = blt + d0 * back_seg
        blh = blk + d1 * back_seg
        blf = blh + d2 * back_seg
        if abs(blf.z) > 0.06:
            blf = Vector((blf.x, blf.y, 0.0))

        t = make_tube(blt, blk,
                      thigh_r  * float(np.random.normal(1.0, 0.06)),
                      thigh_r2 * float(np.random.normal(1.0, 0.06)),
                      verts=8, name=f"back_thigh_{side}")
        if t: parts.append(t)

        # Hindlimb muscles: quadriceps (front), biceps femoris (back, dominant)
        thigh_r_avg = (thigh_r + thigh_r2) * 0.5
        m = make_muscle(blt, blk, thigh_r_avg, (1.0, 0.0, 0.25),
                        t_center=0.50, span=0.65, depth=0.85, width=0.95,
                        name=f"back_quad_{side}")
        if m: parts.append(m)
        m = make_muscle(blt, blk, thigh_r_avg, (-1.0, 0.0, 0.20),
                        t_center=0.45, span=0.85, depth=1.10, width=1.15,
                        name=f"back_hamstring_{side}")
        if m: parts.append(m)

        t = make_tube(blk, blh,
                      calf_r  * float(np.random.normal(1.0, 0.05)),
                      calf_r2 * float(np.random.normal(1.0, 0.05)),
                      verts=8, name=f"back_calf_{side}")
        if t: parts.append(t)

        # Gastrocnemius bulge along the back of the calf
        calf_r_avg = (calf_r + calf_r2) * 0.5
        m = make_muscle(blk, blh, calf_r_avg, (-1.0, 0.0, 0.0),
                        t_center=0.30, span=0.55, depth=1.00, width=1.00,
                        name=f"back_gastroc_{side}")
        if m: parts.append(m)

        t = make_tube(blh, blf,
                      ankle_r * float(np.random.normal(0.95, 0.04)),
                      ankle_r * 0.72 * float(np.random.normal(0.95, 0.04)),
                      verts=7, name=f"back_ankle_{side}")
        if t: parts.append(t)

        # NURBS cleft hoof — replaces 3-ellipsoid stack, port of HoofClaw.
        # BASE at z=blf.z (ground), TIP rises into the lower ankle for voxel-remesh fusion.
        hoof_r = max(0, int(np.random.normal(0.5, 1.0)))
        hoof_back = make_hoof_nurbs(length=hoof_sz * 1.2,
                                    width=hoof_sx * 0.85,
                                    depth_y=hoof_sz * 1.0,
                                    r=hoof_r, name=f"back_hoof_{side}")
        hoof_back.location = blf
        apply_tf(hoof_back)
        parts.append(hoof_back)

        # ── Front leg: shoulder → elbow → ankle ─────────────────────────────
        flt = Vector((front_x, sy, intended_front_z))
        flk = flt + e0 * front_seg
        flh = flk + e1 * front_seg
        flf = flh + e2 * front_seg
        if abs(flf.z) > 0.06:
            flf = Vector((flf.x, flf.y, 0.0))

        t = make_tube(flt, flk,
                      shldr_r  * float(np.random.normal(1.0, 0.06)),
                      shldr_r2 * float(np.random.normal(1.0, 0.06)),
                      verts=8, name=f"front_shoulder_{side}")
        if t: parts.append(t)

        # Forelimb upper muscles: triceps (back, dominant) + biceps brachii (front)
        shldr_r_avg = (shldr_r + shldr_r2) * 0.5
        m = make_muscle(flt, flk, shldr_r_avg, (-1.0, 0.0, 0.0),
                        t_center=0.45, span=0.75, depth=1.05, width=1.10,
                        name=f"front_triceps_{side}")
        if m: parts.append(m)
        m = make_muscle(flt, flk, shldr_r_avg, (1.0, 0.0, 0.0),
                        t_center=0.50, span=0.55, depth=0.65, width=0.85,
                        name=f"front_biceps_{side}")
        if m: parts.append(m)

        t = make_tube(flk, flh,
                      elbow_r  * float(np.random.normal(1.0, 0.05)),
                      elbow_r2 * float(np.random.normal(1.0, 0.05)),
                      verts=8, name=f"front_elbow_{side}")
        if t: parts.append(t)

        # Forearm extensor bulge
        elbow_r_avg = (elbow_r + elbow_r2) * 0.5
        m = make_muscle(flk, flh, elbow_r_avg, (-1.0, 0.0, 0.0),
                        t_center=0.35, span=0.55, depth=0.75, width=0.90,
                        name=f"front_extensor_{side}")
        if m: parts.append(m)

        t = make_tube(flh, flf,
                      fore_r  * float(np.random.normal(0.95, 0.04)),
                      fore_r2 * 0.78 * float(np.random.normal(0.95, 0.04)),
                      verts=7, name=f"front_ankle_{side}")
        if t: parts.append(t)

        # NURBS cleft hoof for the forelimb (same params, independent r per leg)
        hoof_r_f = max(0, int(np.random.normal(0.5, 1.0)))
        hoof_front = make_hoof_nurbs(length=hoof_sz * 1.2,
                                     width=hoof_sx * 0.85,
                                     depth_y=hoof_sz * 1.0,
                                     r=hoof_r_f, name=f"front_hoof_{side}")
        hoof_front.location = flf
        apply_tf(hoof_front)
        parts.append(hoof_front)

    # ── Tail stub ────────────────────────────────────────────────────────────
    # From body tail end (ring 1, top)
    tail_base = Vector(skeleton[1]) + Vector((0, 0, radii[1] * 0.3))
    tail_dir  = Vector((-0.55, 0, 0.45)).normalized()
    tail_tip  = tail_base + tail_dir * float(np.random.normal(0.20, 0.03))
    tail = make_tube(tail_base, tail_tip,
                     float(np.random.normal(0.075, 0.010)),
                     float(np.random.normal(0.030, 0.006)),
                     verts=5, name="tail")
    if tail: parts.append(tail)

    # ── Two-stage join + voxel remesh ────────────────────────────────────────
    # The head carries fine details (eyelid bezier tube, nostril dimples, horn
    # ridges) that the body's coarse voxel size erases. So the head region gets
    # a finer remesh (0.013 m) while the body keeps the cheaper 0.025 m. The
    # two are joined as separate manifolds afterwards — they overlap inside the
    # neck where the head NURBS extends 2 cm into the body NURBS, so the union
    # is visually seamless from outside.
    head_prefixes = (
        "nurbs_head", "eye_", "iris_", "eyelid_",
        "jaw", "nose", "ear_", "horn_",
    )
    valid = [p for p in parts if p is not None]
    head_parts = [p for p in valid if any(p.name.startswith(pre) for pre in head_prefixes)]
    body_parts = [p for p in valid if p not in head_parts]

    def _join_and_remesh(objs, voxel_size, name):
        if not objs: return None
        bpy.ops.object.select_all(action='DESELECT')
        for o in objs: o.select_set(True)
        bpy.context.view_layer.objects.active = objs[0]
        bpy.ops.object.join()
        merged = bpy.context.active_object
        merged.name = name
        activate(merged)
        m = merged.modifiers.new(f"voxel_remesh_{name}", 'REMESH')
        m.mode = 'VOXEL'
        m.voxel_size = voxel_size
        m.adaptivity = 0.0
        bpy.ops.object.modifier_apply(modifier=m.name)
        return merged

    if STYLE == "raw":
        # No remeshing — join parts into one Object as separate manifolds, like
        # infinigen.joining.join_and_rig_parts does. SUBSURF + fur skipped too.
        bpy.ops.object.select_all(action='DESELECT')
        for o in valid: o.select_set(True)
        bpy.context.view_layer.objects.active = valid[0]
        bpy.ops.object.join()
        animal = bpy.context.active_object
        animal.name = "HerbivoreFactory"
        return animal

    head_chunk = _join_and_remesh(head_parts, voxel_size=0.013, name="head_chunk")
    body_chunk = _join_and_remesh(body_parts, voxel_size=0.025, name="body_chunk")

    # Final combine — keep both manifolds, no further voxel pass so head detail survives
    bpy.ops.object.select_all(action='DESELECT')
    if head_chunk is not None:
        head_chunk.select_set(True)
    body_chunk.select_set(True)
    bpy.context.view_layer.objects.active = body_chunk
    bpy.ops.object.join()
    animal = bpy.context.active_object
    animal.name = "HerbivoreFactory"

    apply_mod(animal, 'SUBSURF', levels=2, render_levels=2)

    # Lay direction: head→tail (-X) plus a slight downward tilt for a settled coat;
    # comb_amount + perlin_mag are the configure_hair Combing / Strand Perlin equivalents
    fur_lay = (-0.7, 0.0, -0.2)
    fur = add_fur(animal, n_guides=2200, clump_n=7,
                  length_factor=0.014, clump_radius_factor=0.012,
                  seed=seed + 7000,
                  lay_dir=fur_lay, comb_amount=0.55, perlin_mag=0.10)
    if fur is not None:
        bpy.ops.object.select_all(action='DESELECT')
        animal.select_set(True)
        fur.select_set(True)
        bpy.context.view_layer.objects.active = animal
        bpy.ops.object.join()
        animal = bpy.context.active_object
        animal.name = "HerbivoreFactory"

    return animal

# ── Entry point ──────────────────────────────────────────────────────────────
herbivore = build_herbivore(SEED)
