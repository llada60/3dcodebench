# ── NurbsPart-style decompose / perturb / recompose ──────────────────────────
# Mirrors infinigen.assets.utils.geometry.lofting + parts.generic_nurbs so we can
# perturb skeleton angles / segment proportions / per-ring radii / profile shape
# independently — without this the only randomness is bulk handle noise, which
# loses most of NurbsBody's body-shape diversity.

def _skeleton_to_tangents(skeleton):
    n = skeleton.shape[0]
    axes = np.empty_like(skeleton, dtype=np.float64)
    axes[-1] = skeleton[-1] - skeleton[-2]
    axes[:-1] = skeleton[1:] - skeleton[:-1]
    if n > 2:
        axes[1:-1] = (axes[1:-1] + axes[:-2]) / 2
    norms = np.linalg.norm(axes, axis=-1)
    mask = norms > 0
    axes[mask] = axes[mask] / norms[mask, None]
    return axes


def _rotate_match_directions(a, b):
    """Per-row 3x3 rotation aligning unit a[i] to b[i]; identity where parallel."""
    a = np.asarray(a, dtype=np.float64)
    b = np.asarray(b, dtype=np.float64)
    n = a.shape[0]
    rots = np.tile(np.eye(3), (n, 1, 1))
    cross = np.cross(a, b)
    cross_norm = np.linalg.norm(cross, axis=-1)
    mask = cross_norm > 1e-4
    if not mask.any():
        return rots
    a_n = a[mask] / np.linalg.norm(a[mask], axis=-1, keepdims=True)
    b_n = b[mask] / np.linalg.norm(b[mask], axis=-1, keepdims=True)
    dots = np.clip((a_n * b_n).sum(axis=-1), -1.0, 1.0)
    angles = np.arccos(dots)
    axi = cross[mask] / cross_norm[mask, None]
    K = np.zeros((mask.sum(), 3, 3))
    K[:, 0, 1] = -axi[:, 2]; K[:, 0, 2] =  axi[:, 1]
    K[:, 1, 0] =  axi[:, 2]; K[:, 1, 2] = -axi[:, 0]
    K[:, 2, 0] = -axi[:, 1]; K[:, 2, 1] =  axi[:, 0]
    s = np.sin(angles)[:, None, None]
    c = np.cos(angles)[:, None, None]
    rots[mask] = np.eye(3)[None] + s * K + (1.0 - c) * (K @ K)
    return rots


def _lerp_sample(vec, ts):
    vec = np.asarray(vec, dtype=np.float64)
    ts = np.asarray(ts, dtype=np.float64)
    idx_int = np.floor(ts).astype(np.int64)
    idx_int = np.clip(idx_int, 0, len(vec) - 1)
    idx_rem = ts - idx_int
    res = vec[idx_int].astype(np.float64).copy()
    m = idx_int < (len(vec) - 1)
    if m.any():
        res[m] = (1 - idx_rem[m, None]) * res[m] + idx_rem[m, None] * vec[idx_int[m] + 1]
    return res


def _factorize_handles(handles):
    """(n,m,3) handles → (skeleton (n,3), ts (n,), profiles (n,m,3) in +x-forward local frame)."""
    skeleton = handles.mean(axis=1)
    tangents = _skeleton_to_tangents(skeleton)
    forward = np.tile(np.array([1.0, 0.0, 0.0]), (skeleton.shape[0], 1))
    rot_mats = _rotate_match_directions(tangents, forward)
    profiles = handles - skeleton[:, None]
    profiles = np.einsum("bij,bvj->bvi", rot_mats, profiles)
    ts = np.linspace(0, 1, handles.shape[0])
    return skeleton, ts, profiles


def _compute_profile_verts(skeleton, ts, profiles_xyz):
    """Pose local-frame profiles back into world along skeleton — see lofting.compute_profile_verts."""
    n = profiles_xyz.shape[0]
    k = skeleton.shape[0]
    axes = _skeleton_to_tangents(skeleton)
    sample_idx = ts * (k - 1)
    axes_at_ts = _lerp_sample(axes, sample_idx)
    pos_at_ts = _lerp_sample(skeleton, sample_idx)
    forward = np.tile(np.array([1.0, 0.0, 0.0]), (n, 1))
    rot_mats = _rotate_match_directions(forward, axes_at_ts)
    return np.einsum("bij,bvj->bvi", rot_mats, profiles_xyz) + pos_at_ts[:, None]


def _decompose_params(handles):
    skeleton, ts, profiles = _factorize_handles(handles)
    rads = np.linalg.norm(profiles, axis=2, keepdims=True).mean(axis=1, keepdims=True)
    rads = np.clip(rads, 1e-3, 1e5)  # degenerate caps
    profiles_norm = profiles / rads
    skeleton_root = skeleton[[0]].copy()
    dirs = np.diff(skeleton, axis=0)
    lens = np.linalg.norm(dirs, axis=-1)
    lens_safe = np.maximum(lens, 1e-9)
    length = float(lens.sum())
    proportions = lens / length
    thetas = np.degrees(np.arctan2(dirs[:, 2], dirs[:, 0]))
    skeleton_yoffs = dirs[:, 1] / lens_safe
    return {
        "ts": ts, "rads": rads, "skeleton_root": skeleton_root,
        "skeleton_yoffs": skeleton_yoffs, "length": length,
        "proportions": proportions, "thetas": thetas, "profiles_norm": profiles_norm,
    }


def _recompose_handles(p):
    lens = p["length"] * p["proportions"]
    thetas_rad = np.deg2rad(p["thetas"])
    skeleton_offs = np.stack(
        [lens * np.cos(thetas_rad), lens * p["skeleton_yoffs"], lens * np.sin(thetas_rad)],
        axis=-1,
    )
    skeleton = np.concatenate([p["skeleton_root"], skeleton_offs], axis=0)
    skeleton = np.cumsum(skeleton, axis=0)
    return _compute_profile_verts(skeleton, p["ts"], p["profiles_norm"] * p["rads"])


def _perturb_params(params, rng, var=1.0):
    """Apply NurbsPart.sample_params noise pattern; var<1 dampens all stddevs."""
    p = {k: (v.copy() if isinstance(v, np.ndarray) else v) for k, v in params.items()}
    sd = lambda s: s * var
    sz = float(rng.normal(1, sd(0.1)))
    p["length"] = float(p["length"]) * sz * float(rng.normal(1, sd(0.1)))
    p["rads"] = (p["rads"]
                 * sz * float(rng.normal(1, sd(0.1)))
                 * rng.normal(1, sd(0.15), p["rads"].shape))
    p["proportions"] = p["proportions"] * rng.normal(1, sd(0.15), p["proportions"].shape)
    ang_noise = rng.normal(0, sd(7.0), p["thetas"].shape)
    ang_noise -= ang_noise.mean()  # zero-sum keeps the global orientation
    p["thetas"] = p["thetas"] + ang_noise
    n, m, _ = p["profiles_norm"].shape
    profile_noise = (rng.normal(1, sd(0.07), (1, m, 1))
                     * rng.normal(1, sd(0.15), (n, m, 1)))
    profile_noise[:, : m // 2 - 1] = profile_noise[:, m // 2 : -1][:, ::-1]  # L/R symmetry
    p["profiles_norm"] = p["profiles_norm"] * profile_noise
    return p


