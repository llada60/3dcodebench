def _knots_clamped(degree, n):
    middle = np.linspace(0, n, n - degree + 1)[1:-1]
    knot = np.concatenate([np.zeros(degree + 1), middle, np.full(degree + 1, float(n))])
    return knot / knot.max()


def _knots_uniform(degree, n):
    knot = np.arange(0, n + degree + 1, dtype=float)
    return knot / knot.max()


def add_fur(target_obj, n_guides=2500, clump_n=8, length_factor=0.025,
            clump_radius_factor=0.04, seed=0, lay_dir=None, comb_amount=0.55,
            perlin_mag=0.10, name="fur"):
    """Geometric fur with guides + child hairs forming dense clumps.

    lay_dir / comb_amount port the Combing + Strand Perlin grooming params from
    creature_hair.configure_hair: strand directions are tilted from surface normal
    toward lay_dir (skin-relative), giving the fur a coherent lay rather than the
    fire-spike halo of pure-normal extrusion.
    """
    me = target_obj.data
    me.calc_loop_triangles()
    if len(me.loop_triangles) == 0:
        return None
    tris = np.array([[t.vertices[0], t.vertices[1], t.vertices[2]] for t in me.loop_triangles])
    coords = np.array([v.co for v in me.vertices])
    v0 = coords[tris[:, 0]]
    v1 = coords[tris[:, 1]]
    v2 = coords[tris[:, 2]]
    cross = np.cross(v1 - v0, v2 - v0)
    areas = 0.5 * np.linalg.norm(cross, axis=1)
    if areas.sum() < 1e-9:
        return None
    normals = cross / (np.linalg.norm(cross, axis=1, keepdims=True) + 1e-12)

    bb_size = (coords.max(axis=0) - coords.min(axis=0)).max()
    L = bb_size * length_factor
    clump_r = bb_size * clump_radius_factor

    rng = np.random.RandomState(seed)
    probs = areas / areas.sum()
    sel = rng.choice(len(tris), size=n_guides, p=probs)
    u = rng.uniform(size=n_guides)
    v = rng.uniform(size=n_guides)
    flip = u + v > 1
    u[flip] = 1 - u[flip]; v[flip] = 1 - v[flip]
    w = 1 - u - v
    guide_bases = v0[sel] * w[:, None] + v1[sel] * u[:, None] + v2[sel] * v[:, None]
    guide_normals = normals[sel]

    n_total = n_guides * clump_n
    bases = np.repeat(guide_bases, clump_n, axis=0)
    base_n = np.repeat(guide_normals, clump_n, axis=0)

    up = np.tile([0.0, 0.0, 1.0], (n_total, 1))
    flat_z = np.abs(base_n[:, 2]) > 0.9
    up[flat_z] = np.array([1.0, 0.0, 0.0])
    g_tan = np.cross(base_n, up)
    g_tan /= np.linalg.norm(g_tan, axis=1, keepdims=True) + 1e-12
    g_bitan = np.cross(base_n, g_tan)

    spread_r = rng.uniform(0, 1, n_total) ** 0.5 * clump_r
    spread_a = rng.uniform(0, 2 * np.pi, n_total)
    bases = bases + (np.cos(spread_a)[:, None] * g_tan + np.sin(spread_a)[:, None] * g_bitan) * spread_r[:, None]

    n = base_n + rng.normal(0, perlin_mag, (n_total, 3))
    n /= np.linalg.norm(n, axis=1, keepdims=True) + 1e-12
    if lay_dir is not None:
        lay = np.asarray(lay_dir, dtype=np.float64)
        lay = lay / (np.linalg.norm(lay) + 1e-12)
        # Comb: tilt strand toward lay_dir, keeping a small outward bias from normal
        n = (1.0 - comb_amount) * n + comb_amount * lay[None, :]
        # Re-bias outward so strands don't sink into skin (clamp dot(n, base_n) ≥ 0.15)
        out_dot = (n * base_n).sum(axis=1, keepdims=True)
        n = n + np.maximum(0.15 - out_dot, 0.0) * base_n
        n /= np.linalg.norm(n, axis=1, keepdims=True) + 1e-12
    Ls = L * (0.85 + 0.3 * rng.random(n_total))

    up = np.tile([0.0, 0.0, 1.0], (n_total, 1))
    flat_z = np.abs(n[:, 2]) > 0.9
    up[flat_z] = np.array([1.0, 0.0, 0.0])
    tan = np.cross(n, up)
    tan /= np.linalg.norm(tan, axis=1, keepdims=True) + 1e-12
    bitan = np.cross(n, tan)

    r = (Ls * 0.04)[:, None]
    angles = np.array([0.0, 2.094, 4.189])
    base_pts = [bases + (np.cos(a) * tan + np.sin(a) * bitan) * r for a in angles]
    tip_pts = bases + n * Ls[:, None]

    all_verts = np.zeros((n_total * 4, 3))
    all_verts[0::4] = base_pts[0]
    all_verts[1::4] = base_pts[1]
    all_verts[2::4] = base_pts[2]
    all_verts[3::4] = tip_pts

    bi = np.arange(n_total) * 4
    faces = np.zeros((n_total * 3, 3), dtype=int)
    faces[0::3] = np.column_stack([bi, bi + 1, bi + 3])
    faces[1::3] = np.column_stack([bi + 1, bi + 2, bi + 3])
    faces[2::3] = np.column_stack([bi + 2, bi, bi + 3])

    fur_mesh = bpy.data.meshes.new(name)
    fur_mesh.from_pydata(all_verts.tolist(), [], faces.tolist())
    fur_mesh.update()
    fur_obj = bpy.data.objects.new(name, fur_mesh)
    bpy.context.scene.collection.objects.link(fur_obj)
    return fur_obj


def _eval_nurbs_surface(ctrl_pts, face_size=0.02, cyclic_v=True):
    """Degree-3 NURBS surface evaluation: clamped u, optional cyclic v."""
    n, m, _ = ctrl_pts.shape
    degree = 3
    if cyclic_v:
        ctrl = np.concatenate([ctrl_pts, ctrl_pts[:, :degree, :]], axis=1)
        m_w = m + degree
        kv_base = _knots_uniform(degree, m)
        knots_v = np.append(kv_base, kv_base[1:degree + 1] + kv_base[-1] - kv_base[0])
    else:
        ctrl = ctrl_pts
        m_w = m
        knots_v = _knots_clamped(degree, m)
    knots_u = _knots_clamped(degree, n)
    ulen = np.linalg.norm(np.diff(ctrl_pts, axis=0), axis=-1).sum(axis=0).max()
    vlen = np.linalg.norm(np.diff(ctrl_pts, axis=1), axis=-1).sum(axis=1).max()
    delta = face_size / max(ulen, vlen, 1e-6)
    num_eval = max(20, int(1 / delta) + 1)
    u_params = np.linspace(0, 1, num_eval); u_params[-1] = 1.0 - 1e-10
    if cyclic_v:
        v_params = np.linspace(knots_v[degree], knots_v[m_w], num_eval, endpoint=False)
    else:
        v_params = np.linspace(0, 1, num_eval); v_params[-1] = 1.0 - 1e-10
    bu = BSpline(knots_u, ctrl, degree)
    inter = bu(u_params).transpose(1, 0, 2)
    bv = BSpline(knots_v, inter, degree)
    pts = bv(v_params).transpose(1, 0, 2)
    return pts, num_eval, len(v_params)

