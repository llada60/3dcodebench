def N(m, v=None):
    if v is None:
        v = max(abs(m) * 0.10, 1e-5)
    return float(np.random.normal(m, abs(v)))

def U(a=None, b=None):
    if a is None:
        return float(np.random.uniform(0, 1))
    if b is None:
        return float(np.random.uniform(0, a))
    return float(np.random.uniform(a, b))

def clip_gaussian(mean, std, lo, hi):
    return float(np.clip(np.random.normal(mean, std), lo, hi))

