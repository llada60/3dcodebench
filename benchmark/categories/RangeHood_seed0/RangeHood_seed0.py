GRID_RESOLUTION = 16
GRID_WIDTH = 0.7125
GRID_DEPTH = 0.5225
GRID_CENTER_Y = 0.275
SHELL_THICKNESS = 0.002
SMOOTH_SUBDIVISIONS = 1


def clear_scene():
    bpy.context.scene.cursor.location = (0, 0, 0)
    for obj in list(bpy.data.objects):
        bpy.data.objects.remove(obj, do_unlink=True)
    for collection in (bpy.data.meshes, bpy.data.curves, bpy.data.textures):
        for block in list(collection):
            try:
                collection.remove(block)
            except Exception:
                pass


def select_and_activate(obj):
    bpy.ops.object.select_all(action="DESELECT")
    obj.select_set(True)
    bpy.context.view_layer.objects.active = obj


def apply_all_transforms(obj):
    select_and_activate(obj)
    bpy.ops.object.transform_apply(location=True, rotation=True, scale=True)


def apply_modifier(mod):
    select_and_activate(mod.id_data)
    bpy.ops.object.modifier_apply(modifier=mod.name)


def build_range_hood():
    clear_scene()

    bm = bmesh.new()

    # Build hood body from ring cross-sections
    ring_verts = []
    for ring in RINGS:
        ring_verts.append([bm.verts.new(coord) for coord in ring])

    for bottom, top in zip(ring_verts, ring_verts[1:]):
        count = len(bottom)
        for idx in range(count):
            next_idx = (idx + 1) % count
            bm.faces.new([bottom[idx], bottom[next_idx], top[next_idx], top[idx]])
    bm.faces.new(ring_verts[0])

    # Build vent grille grid
    vent_vertices = []
    for row in range(GRID_RESOLUTION):
        for col in range(GRID_RESOLUTION):
            pos_x = (col / (GRID_RESOLUTION - 1) - 0.5) * GRID_WIDTH
            pos_y = (row / (GRID_RESOLUTION - 1) - 0.5) * GRID_DEPTH + GRID_CENTER_Y
            vent_vertices.append(bm.verts.new((pos_x, pos_y, 0.0)))

    for row in range(GRID_RESOLUTION - 1):
        for col in range(GRID_RESOLUTION - 1):
            base = row * GRID_RESOLUTION + col
            bm.faces.new([
                vent_vertices[base],
                vent_vertices[base + 1],
                vent_vertices[base + GRID_RESOLUTION + 1],
                vent_vertices[base + GRID_RESOLUTION],
            ])

    mesh_data = bpy.data.meshes.new("range_hood")
    bm.to_mesh(mesh_data)
    bm.free()

    hood_obj = bpy.data.objects.new("range_hood", mesh_data)
    bpy.context.collection.objects.link(hood_obj)

    solidify = hood_obj.modifiers.new("solidify", "SOLIDIFY")
    solidify.thickness = SHELL_THICKNESS
    apply_modifier(solidify)

    subdiv = hood_obj.modifiers.new("subsurf", "SUBSURF")
    subdiv.levels = SMOOTH_SUBDIVISIONS
    subdiv.render_levels = SMOOTH_SUBDIVISIONS
    apply_modifier(subdiv)

    hood_obj.rotation_euler = (0, 0, -math.pi / 2)
    apply_all_transforms(hood_obj)
    hood_obj.name = "RangeHoodFactory"
    return hood_obj

build_range_hood()
